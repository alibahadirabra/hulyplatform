          />
        </div>
        <div class="buttonWrapper">
          <Button
            size="small"
            shape="rectangle-left"
            selected={mode === 'closed'}
            label={tracker.string.ClosedSprints}
            on:click={() => handleViewModeChanged('closed')}
          />
        </div>
      </div>
