<script lang="ts">
  import type { Type } from '@hcengineering/core'
  import type {
    ScriptAttribute,
    ScriptTypedPropertyEditorComponentChange,
    ScriptTypedPropertyEditorComponentProps
  } from '@hcengineering/recruit'
  import Textarea from './Textarea.svelte'

  type $$Props = ScriptTypedPropertyEditorComponentProps<Type<string>>

  export let attribute: ScriptAttribute<Type<string>>
  export let value: string
  export let change: ScriptTypedPropertyEditorComponentChange<Type<string>>
</script>

<Textarea
  placeholder=""
  disabled={change === null}
  {value}
  on:change={(event) => {
    if (change !== null && event.detail !== value) {
      void change(event.detail)
    }
  }}
/>
